#ifndef _EnsaParameters
#define _EnsaParameters

class EnsaParameters {
public:
  EnsaParameters(int nsd, int nen, int nfaces, int numflx, int numVars, int numEBC, int numNBC, int fType);

  int getNSD() const { return _nsd; }
  int getNEN() const { return _nen; }
  int getNFACES() const { return _nfaces; }
  int getNUMFLX() const { return _numflx; }
  int getNUMVARS() const { return _numVars; }
  int getNUMEBC() const { return _numEBC; }
  int getNUMNBC() const { return _numNBC; }
  int getFTYPE() const { return _fType; }

private:
  int _nsd, _nen, _nfaces, _numflx, _numVars, _numEBC, _numNBC, _fType;
};

#endif
